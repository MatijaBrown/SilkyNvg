namespace SilkyNvg
{
    /// <summary>
    /// The direction of winding:
    /// CCW: Counter clockwise
    /// CW: Clockwise
    /// </summary>
    public enum Winding
    {
        /// <summary>
        /// Winding for solid shapes.
        /// </summary>
        CCW = 1,
        /// <summary>
        /// Winding for holes.
        /// </summary>
        CW = 2

    }
}